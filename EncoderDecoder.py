class MaskedSoftmaxCELoss(nn.CrossEntropyLoss):
    """带遮蔽的softmax交叉熵损失函数"""
    # pred的形状：(batch_size,num_steps,vocab_size)
    # label的形状：(batch_size,num_steps)
    # valid_len的形状：(batch_size,)
    def forward(self, pred, label, valid_len):
        weights = torch.ones_like(label)
        weights = sequence_mask(weights, valid_len)
        self.reduction='none'
        unweighted_loss = super(MaskedSoftmaxCELoss, self).forward(
            pred.permute(0, 2, 1), label)# shape:(batch_size,num_steps) like label
        #print("unweighted_loss",unweighted_loss,'\nshape',unweighted_loss.shape)
        weighted_loss = (unweighted_loss * weights).mean(dim=1)#batch 中填充的词计算的损失不算
        return weighted_loss # (batch_size,)
